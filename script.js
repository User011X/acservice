  // მობილური მენიუს გახსნა/დახურვა
  const burgerBtn = document.querySelector('.burger');
  const mobileNav = document.getElementById('mobileNav');
  const mobileNavBackdrop = document.getElementById('mobileNavBackdrop');

  function toggleMobileNav() {
    const isOpen = mobileNav.classList.contains('open');
    if (isOpen) {
      closeMobileNav();
    } else {
      openMobileNav();
    }
  }

  function openMobileNav() {
    mobileNav.classList.add('open');
    mobileNavBackdrop.classList.add('open');
    burgerBtn.classList.add('active');
    burgerBtn.setAttribute('aria-expanded', 'true');
    document.body.classList.add('nav-locked');
  }

  function closeMobileNav() {
    mobileNav.classList.remove('open');
    mobileNavBackdrop.classList.remove('open');
    burgerBtn.classList.remove('active');
    burgerBtn.setAttribute('aria-expanded', 'false');
    document.body.classList.remove('nav-locked');
  }

  // ეკრანის ზომის შეცვლისას (მაგ. ტელეფონიდან დესკტოპზე rotate/resize), მენიუს დახურვა
  window.addEventListener('resize', () => {
    if (window.innerWidth > 960) closeMobileNav();
  });

  // ენის შეცვლის ფუნქციონალი
  const langButtons = document.querySelectorAll('[data-set-lang]');
  langButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      const targetLang = btn.getAttribute('data-set-lang');
      
      langButtons.forEach(b => {
        b.classList.toggle('active', b.getAttribute('data-set-lang') === targetLang);
      });
      
      document.documentElement.setAttribute('lang', targetLang);
      document.documentElement.setAttribute('data-lang', targetLang);
      
      document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[targetLang] && translations[targetLang][key]) {
          if(element.querySelector('em') || translations[targetLang][key].includes('<em>')) {
            element.innerHTML = translations[targetLang][key];
          } else {
            element.textContent = translations[targetLang][key];
          }
        }
      });
    });
  });

  // FAQ Accordion Functionality
  document.querySelectorAll('.faq-trigger').forEach(trigger => {
    trigger.addEventListener('click', () => {
      const parent = trigger.parentElement;
      const isActive = parent.classList.contains('active');
      
      document.querySelectorAll('.faq-item').forEach(item => item.classList.remove('active'));
      
      if (!isActive) {
        parent.classList.add('active');
      }
    });
  });

  // Scroll-ზე გამოჩენის ანიმაცია (fade-up)
  document.querySelectorAll('.services, .process, .why, .faq').forEach(section => {
    const items = section.querySelectorAll('.reveal');
    items.forEach((el, i) => {
      el.style.transitionDelay = (i % 4) * 0.09 + 's';
    });
  });
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15 });
  document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

  // WhatsApp ღილაკი: დამალული hero სექციაში, ჩნდება პირველივე სქროლზე
  const waFloat = document.querySelector('.whatsapp-float');
  const heroSection = document.getElementById('top');
  if (waFloat && heroSection) {
    const heroObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        // hero აღარ ჩანს ეკრანზე => ღილაკი გამოჩნდეს
        waFloat.classList.toggle('visible', !entry.isIntersecting);
      });
    }, { threshold: 0, rootMargin: '-1px 0px 0px 0px' });
    heroObserver.observe(heroSection);

    let waScrollTimeout;
    window.addEventListener('scroll', () => {
      if (!waFloat.classList.contains('visible')) return;
      waFloat.classList.add('is-scrolling');
      clearTimeout(waScrollTimeout);
      waScrollTimeout = setTimeout(() => {
        waFloat.classList.remove('is-scrolling');
      }, 400);
    }, { passive: true });
  }

