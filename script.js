const burgerBtn = document.querySelector('.burger');
const mobileNav = document.getElementById('mobileNav');
const mobileNavBackdrop = document.getElementById('mobileNavBackdrop');

function toggleMobileNav() {
  const isOpen = mobileNav.classList.contains('open');
  if (isOpen) {
    closeMobileNav();
  } else {
    openMobileNav();
  }
}

function openMobileNav() {
  mobileNav.style.willChange = 'transform';
  mobileNav.classList.add('open');
  mobileNavBackdrop.classList.add('open');
  burgerBtn.classList.add('active');
  burgerBtn.setAttribute('aria-expanded', 'true');
  document.body.classList.add('nav-locked');
}

function closeMobileNav() {
  mobileNav.classList.remove('open');
  mobileNavBackdrop.classList.remove('open');
  burgerBtn.classList.remove('active');
  burgerBtn.setAttribute('aria-expanded', 'false');
  document.body.classList.remove('nav-locked');
  mobileNav.addEventListener('transitionend', function clearWillChange() {
    mobileNav.style.willChange = 'auto';
    mobileNav.removeEventListener('transitionend', clearWillChange);
  });
}

window.addEventListener('resize', () => {
  if (window.innerWidth > 960 && mobileNav.classList.contains('open')) {
    closeMobileNav();
  }
}, { passive: true });

const langButtons = document.querySelectorAll('[data-set-lang]');
const i18nElements = document.querySelectorAll('[data-i18n]');

langButtons.forEach(btn => {
  btn.addEventListener('click', () => {
    const targetLang = btn.dataset.setLang;
    const dictionary = translations[targetLang];

    if (!dictionary) return;

    // Active button
    langButtons.forEach(button => {
      button.classList.toggle(
        'active',
        button.dataset.setLang === targetLang
      );
    });

    // Update HTML language
    document.documentElement.lang = targetLang;
    document.documentElement.dataset.lang = targetLang;

    // Translate page
    i18nElements.forEach(element => {
      const key = element.dataset.i18n;
      const value = dictionary[key];

      if (!value) return;

      if (element.querySelector('em') || value.includes('<em>')) {
        element.innerHTML = value;
      } else {
        element.textContent = value;
      }
    });
  });
});

